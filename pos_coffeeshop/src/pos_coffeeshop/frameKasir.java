    private int getIdProdukByNama(String namaProduk) throws SQLException {
        Connection conn = Koneksi.getConnection();
        PreparedStatement ps = conn.prepareStatement("SELECT id_produk FROM produk WHERE nama_produk = ?");
        ps.setString(1, namaProduk);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            return rs.getInt("id_produk");
        } else {
            throw new SQLException("Produk tidak ditemukan");
        }
    }
    
    private void loadProduk() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Produk");
        model.addColumn("Harga");
        model.addColumn("Kategori");

        try {
            Connection conn = Koneksi.getConnection();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM produk");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_produk"),
                    rs.getString("nama_produk"),
                    rs.getDouble("harga"),
                    rs.getString("tipe")
                });
            }
            TABEL.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load produk: " + e.getMessage());
        }
    }
    
    private void hitungTotalBayar() {
        double total = 0;
        for (int i = 0; i < keranjang.getRowCount(); i++) {
            total += Double.parseDouble(keranjang.getValueAt(i, 4).toString());
        }
        txtTotalBayar.setText(String.valueOf(total));
    }

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (keranjang.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Keranjang masih kosong!");
                return;
            }

            String namaPelanggan = txtPelanggan.getText().isEmpty() ? "Customer" : txtPelanggan.getText();
            double totalBayar = Double.parseDouble(txtTotalBayar.getText());

            Connection conn = Koneksi.getConnection();

            // Simpan ke tabel transaksi
            PreparedStatement psTrans = conn.prepareStatement(
                "INSERT INTO transaksi (total_bayar, nama_pelanggan) VALUES (?, ?)",
                Statement.RETURN_GENERATED_KEYS
            );
            psTrans.setDouble(1, totalBayar);
            psTrans.setString(2, namaPelanggan);
            psTrans.executeUpdate();

            ResultSet rs = psTrans.getGeneratedKeys();
            int idTransaksi = 0;
            if (rs.next()) {
                idTransaksi = rs.getInt(1);
            }

            // Simpan ke detail_transaksi dari isi tabel keranjang
            for (int i = 0; i < keranjang.getRowCount(); i++) {
                int idProduk = Integer.parseInt(keranjang.getValueAt(i, 0).toString());
                int jumlah = Integer.parseInt(keranjang.getValueAt(i, 3).toString());
                double subtotal = Double.parseDouble(keranjang.getValueAt(i, 4).toString());

                PreparedStatement psDetail = conn.prepareStatement(
                    "INSERT INTO detail_transaksi (transaksi_id, produk_id, jumlah, subtotal) VALUES (?, ?, ?, ?)"
                );
                psDetail.setInt(1, idTransaksi);
                psDetail.setInt(2, idProduk);
                psDetail.setInt(3, jumlah);
                psDetail.setDouble(4, subtotal);
                psDetail.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan!");

            // Kosongkan semua input
            keranjang.setRowCount(0);
            txtNamaProduk.setText("");
            txtHarga.setText("");
            txtJumlah.setText("");
            txtPelanggan.setText("");
            txtTotalBayar.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

