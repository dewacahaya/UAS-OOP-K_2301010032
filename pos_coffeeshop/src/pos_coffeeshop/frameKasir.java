    private void btnTambahKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahKeranjangActionPerformed
        try {
            String namaProduk = txtNamaProduk.getText();
            double harga = Double.parseDouble(txtHarga.getText());
            int jumlah = Integer.parseInt(txtJumlah.getText());

            double subtotal = harga * jumlah;
            int idProduk = getIdProdukByNama(namaProduk); // Pastikan fungsi ini sudah ada

            // Tambahkan ke modelKeranjang
            keranjang.addRow(new Object[]{
                idProduk,
                namaProduk,
                harga,
                jumlah,
                subtotal
            });

            // Hitung ulang total bayar
            hitungTotalBayar();

            // Reset jumlah aja
            txtJumlah.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data tidak valid: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahKeranjangActionPerformed

